# ASCII is American Standard Code for Information Interchange.
# eg: 1 char to num
character = input("Enter an Alphabet to find it's ASCII value:")
print("The Ascii value of" ,character, "is :", ord(character))


# eg: 2 num to char
num = int(input("Enter Number to find it's ASCII value:"))
print("The Ascii value of", num, "is :", chr(num))